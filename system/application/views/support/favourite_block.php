<h2>What does 'favourite' mean?</h2>
<p>
You can add Cloudscapes, Clouds and links that you have found useful or interesting as favourites. This facility serves a number of functions:
</p>
<ul class="arrows">
<li>Lets you know what other people have found interesting</li>
<li>Acts as a way of marking things you have found interesting (things you have favourited will appear on your profile page)</li>
</ul>
<p>You need to have a reputation of at least 5 on the site in order to be able to add favourites. </p><p>You can gain reputation by people marking
your Clouds or Cloudscapes as favourites or by recommending links that you have added to Clouds.</p>
<h2>How is 'favouriting' something different to 'following' it?</h2>
<p>
When you decide to follow a Cloud or Cloudscape you will receive cloudstream updates every time something new is added. This ensures that you don't miss any new additions or activity. 
</p>
<p>
When you favourite a Cloud or Cloudscape you are indicating that you have found something interesting that you would like to flag up. It is possible to both follow and favourite a Cloud or Cloudscape.
</p>
